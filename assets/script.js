function applyFilters() {
    const cat = document.getElementById('filterCategory').value;
    const minP = parseInt(document.getElementById('filterMinPrice').value) || 0;
    const maxP = parseInt(document.getElementById('filterMaxPrice').value) || Infinity;
    let list;
    if (cat === 'tools') list = TOOLS;
    else if (cat === 'ingredients') list = INGREDIENTS;
    else list = TOOLS.concat(INGREDIENTS);
    CURRENT_LIST = list;
    let filtered = list.filter(p => p.price >= minP && p.price <= maxP);
    renderProducts(filtered);
}

