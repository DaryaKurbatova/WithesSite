let CURRENT_LIST = TOOLS.concat(INGREDIENTS);

const grid = document.getElementById('productGrid');
const cartCount = document.getElementById('cartCount');

function getCart() {
    return JSON.parse(localStorage.getItem('witchCart') || '{}');
}
function setCart(cart) {
    localStorage.setItem('witchCart', JSON.stringify(cart));
}
function updateCartCount() {
    const cart = getCart();
    const count = Object.keys(cart).length;
    cartCount.textContent = count;
}

function renderProducts(products) {
    grid.innerHTML = "";
    for (let i = 0; i < products.length; i++) {
        const product = products[i];
        const card = document.createElement('div');
        card.className = 'product-card';
        card.style.backgroundImage = product.img ? `url('${product.img}')` : "none";
        card.style.backgroundSize = "cover";
        card.style.backgroundPosition = "center";
        card.style.backgroundRepeat = "no-repeat";

        card.innerHTML = `
            <div class="product-title-scroll">
                <span>${product.name}</span>
            </div>
            <div class="product-card-bottom">
                <div class="product-price">
                    ${product.price} <span class="crystal-icon">🟪</span> <span class="crystal-label">Кристаллы</span>
                </div>
                <div class="quantity-selector">
                    <button class="q-minus" aria-label="Уменьшить количество">-</button>
                    <input type="text" class="q-value" value="1" maxlength="3" readonly>
                    <button class="q-plus" aria-label="Увеличить количество">+</button>
                </div>
                <button data-id="${i}" class="add-to-cart-btn">В корзину</button>
            </div>
        `;

        card.addEventListener('click', function(e) {
            if (
                e.target.closest('.add-to-cart-btn') ||
                e.target.closest('.q-minus') ||
                e.target.closest('.q-plus')
            ) return;
            openProductModal(product, i);
        });

        const minusBtn = card.querySelector('.q-minus');
        const plusBtn = card.querySelector('.q-plus');
        const valueInput = card.querySelector('.q-value');
        let quantity = 1;

        minusBtn.onclick = function(e) {
            e.stopPropagation();
            if (quantity > 1) {
                quantity--;
                valueInput.value = quantity;
            }
        };
        plusBtn.onclick = function(e) {
            e.stopPropagation();
            if (quantity < 100) {
                quantity++;
                valueInput.value = quantity;
            }
        };

        card.querySelector('.add-to-cart-btn').onclick = function(e) {
            e.stopPropagation();
            let key = '';
            if (CURRENT_LIST === TOOLS) key = 'tools-' + i;
            else if (CURRENT_LIST === INGREDIENTS) key = 'ingredients-' + i;
            else if (TOOLS.includes(product)) key = 'tools-' + TOOLS.indexOf(product);
            else if (INGREDIENTS.includes(product)) key = 'ingredients-' + INGREDIENTS.indexOf(product);
            else key = 'all-' + i;
            const cart = getCart();
            cart[key] = (cart[key] || 0) + quantity;
            setCart(cart);
            updateCartCount();
            this.textContent = "В корзине!";
            this.disabled = true;
            setTimeout(() => {
                this.textContent = "В корзину";
                this.disabled = false;
            }, 1000);
        };
        grid.appendChild(card);
    }
}

